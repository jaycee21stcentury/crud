//these are for the odd numbers
//while
let j = 1;
while (j < 50) {
    if (j % 2 !== 0) {
        console.log(j);
    }
    j++;
}
//for loop
for (let j = 1; j < 50; j++) {
    if (j % 2 !== 0) {
        console.log(j);
    }
}
//do while
let c = 1;
do {
    if (c % 2 !== 0) {
        console.log(c);
    }
    c++;
} while (c < 50);






